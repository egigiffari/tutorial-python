# we've added a variable named `message`
# every variable is connected to a `value`
message = "Hello Python Crash Course reader!"
print(message)

# you can change the value of variable
message = "Hello Python Crash Course World!"
print(message)


# naming variabel rules:
# variable names can contain only `letters, numbers, and underscores`. `message_1` but `1_message`
# spaces are not allowed. `greeting_message` but `greeting message`
# variable names should be short but descriptive. `name` but `n`, `student_name` but `s_n`, `name_length` but `length_of_persons_name`